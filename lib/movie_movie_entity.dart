
class Movie {
  final String title;
  final String thumbnail;
  final List<String> genreList;
  final String image;
  final String description;
  final String trailer;
  final String id;

  Movie({
    required this.title,
    required this.thumbnail,
    required this.genreList,
    required this.image,
    required this.description,
    required this.trailer,
    required this.id,
  });

}